class B1 
{
	public static void main(String[] args) 
	{   
		int x=20;
		System.out.println(x); //20
		System.out.println(x++);//20
		System.out.println(++x);//22
		System.out.println(x); //22
		System.out.println(x--);  //22
		System.out.println(--x); //20
		System.out.println(x); //20
	}
}
